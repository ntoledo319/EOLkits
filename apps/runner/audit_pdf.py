def generate_audit_pdf(
    upload_url: Optional[str],
    email: str,
    deadline: Optional[str] = None,
    output_path: Optional[str] = None,
    upload_path: Optional[str] = None,
) -> str:
    """Generate an audit PDF report."""
    package = generate_audit_package(upload_url, email, deadline, output_path, upload_path)
    return package["pdf_path"]


def generate_audit_package(
    upload_url: Optional[str],
    email: str,
    deadline: Optional[str] = None,
    output_path: Optional[str] = None,
    upload_path: Optional[str] = None,
) -> dict:
    """Generate an audit PDF plus delivery metadata for the worker."""
    from weasyprint import HTML

    input_content = _read_input(upload_path, upload_url)
    input_hash = hashlib.sha256(input_content).hexdigest()
    findings = _scan_input(input_content)

    # Sort by severity * blast_radius
    findings.sort(key=lambda f: f["severity"] * f["blast_radius"], reverse=True)
    categories = _summarize_categories(findings)
    rule_pack_version = "2026.06.05"

    template = Template(PDF_TEMPLATE)
    html_content = template.render(
        generated_at=datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"),
        rule_pack_version=rule_pack_version,
        input_hash=input_hash,
        total_findings=len(findings),
        findings=findings,
        categories=categories,
        cost_estimate="12,500" if deadline else None,
        upcoming_deprecations=[
            {"name": "Amazon Linux 2 EOL", "date": "2026-06-30"},
            {"name": "Lambda Python 3.9 EOL", "date": "2026-10-31"},
        ],
        verify_url=f"{PUBLIC_SITE_URL}/verify/?hash={input_hash}",
    )

    # Generate PDF
    if output_path is None:
        output_path = f"/tmp/audit_{input_hash[:8]}.pdf"

    HTML(string=html_content).write_pdf(output_path)

    # Embed metadata
    # WeasyPrint allows setting PDF metadata including custom properties
    # The hash is also visible in the PDF content itself

    with open(output_path, "rb") as f:
        pdf_bytes = f.read()

    return {
        "pdf_path": output_path,
        "pdf_base64": base64.b64encode(pdf_bytes).decode("ascii"),
        "email": email,
        "input_hash": input_hash,
        "rule_pack_version": rule_pack_version,
        "generated_at": datetime.utcnow().isoformat() + "Z",
        "findings_count": len(findings),
    }


def _read_input(upload_path: Optional[str], upload_url: Optional[str]) -> bytes:
    """Resolve the audit input.

    Prefers a server-resolved local path (inline runner). Falls back to an
    HTTP(S) URL that the API derived from a validated upload_id — never an
    arbitrary client-supplied URL. The URL fetch is SSRF-guarded so a malicious
    upload reference cannot make the runner hit internal/metadata endpoints.
    """
    if upload_path:
        return _read_local_input(upload_path)
    if upload_url:
        return _download_input(upload_url)
    raise ValueError("upload_path or upload_url is required")


def _read_local_input(path: str) -> bytes:
    if not os.path.isfile(path):
        raise ValueError("upload not found")
    if os.path.getsize(path) > MAX_INPUT_BYTES:
        raise ValueError("upload too large")
    with open(path, "rb") as fh:
        return fh.read()


def _assert_public_host(hostname: str) -> None:
    """Block SSRF to private / loopback / link-local / metadata addresses."""
    try:
        infos = socket.getaddrinfo(hostname, None)
    except socket.gaierror as exc:
        raise ValueError(f"cannot resolve upload host: {hostname}") from exc
    for info in infos:
        ip = ipaddress.ip_address(info[4][0])
        if (
            ip.is_private
            or ip.is_loopback
            or ip.is_link_local
            or ip.is_reserved
            or ip.is_multicast
            or ip.is_unspecified
        ):
            raise ValueError(f"refusing to fetch upload from non-public address: {ip}")


def _download_input(upload_url: Optional[str]) -> bytes:
    if not upload_url:
        raise ValueError("upload_url is required")
    parsed = urlparse(upload_url)
    if parsed.scheme not in ("http", "https"):
        raise ValueError("upload_url must be http(s)")
    if not parsed.hostname:
        raise ValueError("upload_url missing host")
    _assert_public_host(parsed.hostname)
    response = requests.get(upload_url, timeout=30, stream=True)
    response.raise_for_status()
    chunks = bytearray()
    for chunk in response.iter_content(8192):
        chunks.extend(chunk)
        if len(chunks) > MAX_INPUT_BYTES:
            raise ValueError("upload too large")
    return bytes(chunks)


def _scan_input(input_content: bytes) -> list[dict]:
    text = input_content.decode("utf-8", errors="ignore")
    checks = [
        ("nodejs20.x", 9, "critical", 0.9, "Lambda Node.js 20 runtime deprecated", "Update to Node.js 22 using lambda-lifeline"),
        ("Runtime: python3.9", 7, "high", 0.7, "Lambda Python 3.9 runtime deprecated", "Update to Python 3.12 using python-pivot"),
        ("Runtime: python3.10", 5, "medium", 0.6, "Lambda Python 3.10 runtime requires migration planning", "Validate Python 3.12 readiness using python-pivot"),
        ("Runtime: python3.11", 4, "medium", 0.5, "Lambda Python 3.11 runtime requires migration planning", "Validate Python 3.12 readiness using python-pivot"),
        ("amazonlinux:2", 8, "critical", 0.8, "Amazon Linux 2 container base image detected", "Move the base image to AL2023 using al2023-gate"),
        ("amazon-linux-2", 8, "critical", 0.8, "Amazon Linux 2 AMI reference detected", "Move launch templates and AMIs to AL2023 using al2023-gate"),
        ("aws-sdk", 5, "medium", 0.5, "aws-sdk v2 usage may block Node.js 22 migration", "Migrate to modular AWS SDK v3 clients"),
    ]
    findings = []
    for needle, severity, severity_class, blast_radius, title, remediation in checks:
        if needle in text:
            findings.append(
                {
                    "severity": severity,
                    "severity_class": severity_class,
                    "blast_radius": blast_radius,
                    "title": title,
                    "description": f"Detected `{needle}` in uploaded audit input.",
                    "location": "uploaded input",
                    "remediation": remediation,
                }
            )
    if not findings:
        findings.append(
            {
                "severity": 1,
                "severity_class": "low",
                "blast_radius": 0.1,
                "title": "No known deprecated AWS runtime patterns detected",
                "description": "The uploaded input did not match the current public rule pack.",
                "location": "uploaded input",
                "remediation": "Review the report and run the relevant EOLkits CLI for a deeper repository scan.",
            }
        )
    return findings


def _summarize_categories(findings: list[dict]) -> list[dict]:
    buckets: dict[str, list[dict]] = {}
    for finding in findings:
        if "runtime" in finding["title"].lower():
            key = "Runtime Deprecations"
        elif "amazon linux" in finding["title"].lower():
            key = "Amazon Linux"
        elif "aws-sdk" in finding["title"].lower():
            key = "Dependency Issues"
        else:
            key = "General Findings"
        buckets.setdefault(key, []).append(finding)
    return [
        {
            "name": name,
            "count": len(items),
            "risk_score": round(sum(i["severity"] * i["blast_radius"] for i in items) * 10),
        }
        for name, items in buckets.items()
    ]
